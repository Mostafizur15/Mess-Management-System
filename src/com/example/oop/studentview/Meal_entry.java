    public void Load(){
        meal_template_imp dao=new meal_template_imp();
        List<Meal> list=dao.list();
        DefaultTableModel DFT=(DefaultTableModel) jTable1.getModel();
        DFT.setRowCount(0);
        for(Meal st:list){
            String Date=st.getDate();
            String Name=st.getName();
            int Id=st.getID();
            int Morning=st.getMorning();
            int Launch=st.getLaunch();
            int Dinner=st.getDinner();
            Object[] rowData={Date,Name,Id,Morning,Launch,Dinner};
            DFT.addRow(rowData);
        }
    }
