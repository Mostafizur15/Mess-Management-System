    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try{
            String input = JOptionPane.showInputDialog("Enter the Student ID and Date (separated by comma):");
            String[] values = input.split(",");
            find = Integer.parseInt(values[0].trim());
            FindDate = values[1].trim();
            meal_template_imp ob=new meal_template_imp();

            Meal st=ob.get(find,FindDate);
            if(st.getName()==null){
                mark=false;
                return;
            }
            //System.out.println(st.getFname());
            textsdate.setText(st.getDate());
            textsname.setText(st.getName());
            textsId.setText(Integer.toString(st.getID()));
            textsmorning.setText(Integer.toString(st.getMorning()));
            textslaunch.setText(Integer.toString(st.getLaunch()));
            textsdinner.setText(Integer.toString(st.getDinner()));
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,"Error"); 
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed
